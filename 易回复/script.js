    // 解析OCR结果并返回解析后的数据
    function parseOCRResult(text) {
        // 简单的解析逻辑，根据常见的作业记录格式进行匹配
        const lines = text.split('\n').map(line => line.trim()).filter(line => line);
        
        console.log('解析后的行:', lines);
        
        const result = {
            chinese: '',
            math: '',
            extra: ''
        };
        
        // 识别学生姓名
        const nameMatch = lines.find(line => line.includes('姓名') || line.includes('名字') || line.includes('学生'));
        if (nameMatch) {
            console.log('学生姓名:', nameMatch);
        }
        
        // 解析语文作业
        let chineseLines = [];
        let inChineseSection = false;
        lines.forEach(line => {
            if (line.includes('语文') || line.includes('语文作业')) {
                inChineseSection = true;
                chineseLines.push(line);
            } else if (inChineseSection && (line.includes('数学') || line.includes('数学作业') || line.includes('加练') || line.includes('英语'))) {
                inChineseSection = false;
            } else if (inChineseSection) {
                chineseLines.push(line);
            }
        });
        if (chineseLines.length > 0) {
            result.chinese = chineseLines.join(' ').replace('语文', '').replace('语文作业', '').trim();
        }
        
        // 解析数学作业
        let mathLines = [];
        let inMathSection = false;
        lines.forEach(line => {
            if (line.includes('数学') || line.includes('数学作业')) {
                inMathSection = true;
                mathLines.push(line);
            } else if (inMathSection && (line.includes('语文') || line.includes('加练') || line.includes('英语'))) {
                inMathSection = false;
            } else if (inMathSection) {
                mathLines.push(line);
            }
        });
        if (mathLines.length > 0) {
            result.math = mathLines.join(' ').replace('数学', '').replace('数学作业', '').trim();
        }
        
        // 解析加练/英语作业
        let extraLines = [];
        let inExtraSection = false;
        lines.forEach(line => {
            if (line.includes('加练') || line.includes('额外') || line.includes('英语')) {
                inExtraSection = true;
                extraLines.push(line);
            } else if (inExtraSection && (line.includes('语文') || line.includes('数学'))) {
                inExtraSection = false;
            } else if (inExtraSection) {
                extraLines.push(line);
            }
        });
        if (extraLines.length > 0) {
            result.extra = extraLines.join(' ').trim();
        }
        
        // 特殊处理：如果没有明确的章节划分，尝试根据关键词匹配
        if (!result.chinese && !result.math && !result.extra) {
            // 简单的关键词匹配
            lines.forEach(line => {
                if (line.includes('抄写') || line.includes('语文')) {
                    result.chinese = line;
                } else if (line.includes('数学') || line.includes('计算') || line.includes('正式')) {
                    result.math = line;
                } else if (line.includes('英语') || line.includes('加练') || line.includes('额外')) {
                    result.extra = line;
                }
            });
        }
        
        // 处理对勾和未完成标记
        if (result.chinese) {
            // 检查是否包含对勾或完成标记
            if (result.chinese.includes('√') || result.chinese.includes('对勾') || result.chinese.includes('完成')) {
                result.chinese = result.chinese + ' (已完成)';
            } else if (result.chinese.includes('×') || result.chinese.includes('未完成') || result.chinese.includes('不太熟')) {
                result.chinese = result.chinese + ' (未完成)';
            }
        }
        
        if (result.math) {
            if (result.math.includes('√') || result.math.includes('对勾') || result.math.includes('完成')) {
                result.math = result.math + ' (已完成)';
            } else if (result.math.includes('×') || result.math.includes('未完成')) {
                result.math = result.math + ' (未完成)';
            }
        }
        
        if (result.extra) {
            if (result.extra.includes('√') || result.extra.includes('对勾') || result.extra.includes('完成')) {
                result.extra = result.extra + ' (已完成)';
            } else if (result.extra.includes('×') || result.extra.includes('未完成')) {
                result.extra = result.extra + ' (未完成)';
            }
        }
        
        console.log('解析结果:', result);
        return result;
    }
    

